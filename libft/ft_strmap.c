#include "libft.h"

char	*ft_strmap(char const *s, char (*f)(char))
{
	char	*str;
	int		y;
	int		i;

	if (!s)
		return (NULL);
	i = 0;
	y = ft_strlen(s);
	if (!(str = ft_memalloc(y + 1)))
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}

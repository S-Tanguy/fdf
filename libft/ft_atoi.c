#include "libft.h"

int		ft_atoi(const char *s)
{
	int		i;
	int		neg;
	int		nb;

	i = 0;
	nb = 0;
	while (s[i] == '\f' || s[i] == '\r' || s[i] == '\t' || s[i] == '\v'
			|| s[i] == '\n' || s[i] == ' ')
		i++;
	if (s[i] == '-')
		neg = 1;
	if (s[i] == '+' || s[i] == '-')
		i++;
	while (s[i] >= '0' && s[i] <= '9')
	{
		nb = nb * 10 + (s[i] - '0');
		i++;
	}
	if (neg == 1)
		return (-nb);
	else
		return (nb);
}

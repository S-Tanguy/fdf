#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	char			*str;
	unsigned int	y;
	int				i;

	if (!s)
		return (NULL);
	i = 0;
	y = ft_strlen((char *)s);
	if (!(str = ft_memalloc(y + 1)))
		return (NULL);
	while (s[i] != '\0')
	{
		str[i] = f(i, s[i]);
		i++;
	}
	str[i] = '\0';
	return (str);
}
